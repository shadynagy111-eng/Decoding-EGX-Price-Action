class RollingWindowDataset(Dataset):
    def __init__(self, df, image_dir, transform=None):
        self.transform = transform
        self.samples = []
        missing = 0
        for _, row in df.iterrows():
            fname = f"{row['ticker']}_{int(row['window_row_start'])}_{int(row['window_row_end'])}.png"
            path  = os.path.join(image_dir, row['label'], fname)
            if os.path.exists(path):
                self.samples.append((path, LABEL_TO_IDX[row['label']], float(row['pct_change'])))
            else:
                missing += 1
        if missing:
            print(f'      {missing} image files not found and skipped.')

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        path, label, _ = self.samples[idx]
        img = Image.open(path).convert('RGB')
        if self.transform:
            img = self.transform(img)
        return img, label

    @property
    def pct_changes(self):
        return [s[2] for s in self.samples]

    @property
    def targets(self):
        return [s[1] for s in self.samples]


