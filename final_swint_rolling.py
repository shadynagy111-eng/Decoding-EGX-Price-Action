def financial_metrics(test_preds, pct_changes, fwd_dates=None):
    """
    Portfolio-level financial metrics.
    - Sharpe: daily portfolio returns (mean across stocks per day), active days only.
    - MDD: relative drawdown on compounded daily portfolio equity curve.
    - Profit factor, win rate, total trades: individual active trades.
    """
    FWD_DAYS = 5
    positions     = np.zeros(len(test_preds))
    positions[test_preds == LABEL_TO_IDX['Bullish']] =  1.0
    positions[test_preds == LABEL_TO_IDX['Bearish']] = -1.0
    trade_returns = positions * pct_changes

    # ── Portfolio daily returns ───────────────────────────────────────────────
    if fwd_dates is not None:
        dates    = pd.to_datetime(fwd_dates)
        daily_df = pd.DataFrame({'ret': trade_returns, 'date': dates})
        daily    = daily_df.groupby('date')['ret'].mean().sort_index().values
    else:
        daily = trade_returns

    # ── Sharpe on active days only ────────────────────────────────────────────
    active_daily = daily[daily != 0]
    if len(active_daily) >= 2 and active_daily.std() > 0:
        sharpe = float((active_daily.mean() / active_daily.std()) * np.sqrt(252 / FWD_DAYS))
    else:
        sharpe = 0.0

    # ── MDD on compound equity curve ──────────────────────────────────────────
    equity       = np.cumprod(1 + daily)
    peak         = np.maximum.accumulate(equity)
    max_drawdown = float(((equity - peak) / peak).min())

    # ── Trade-level metrics ───────────────────────────────────────────────────
    active        = trade_returns[trade_returns != 0]
    pos_ret       = trade_returns[trade_returns > 0].sum()
    neg_ret       = trade_returns[trade_returns < 0].sum()
    profit_factor = float(pos_ret / abs(neg_ret)) if neg_ret != 0 else float('inf')
    total_trades  = int(len(active))
    win_rate      = float((active > 0).sum() / total_trades) if total_trades > 0 else 0.0

    return sharpe, max_drawdown, profit_factor, win_rate, total_trades


