class AI4FinSwinT(nn.Module):
    """Swin-Tiny wrapper for 3-class financial chart classification.

    Architecture notes
    ------------------
    torchvision's Swin-T `features` has 8 sub-modules:
        [0] Patch embedding  (Conv2d 4×4 stride 4 + LayerNorm)
        [1] Stage 1          (2 × SwinTransformerBlock, 96-d)
        [2] Patch merging 1  (downsample 2×, 96 → 192)
        [3] Stage 2          (2 × SwinTransformerBlock, 192-d)
        [4] Patch merging 2  (downsample 2×, 192 → 384)
        [5] Stage 3          (6 × SwinTransformerBlock, 384-d)
        [6] Patch merging 3  (downsample 2×, 384 → 768)
        [7] Stage 4          (2 × SwinTransformerBlock, 768-d)

    Classifier head:  norm → adaptive_avg_pool → flatten → head(Linear)
    We replace `head` with Dropout(0.5) → Linear(768, 3).
    """

    def __init__(self):
        super().__init__()
        base = swin_t(weights=Swin_T_Weights.IMAGENET1K_V1)

        # ── Replace classifier head ──────────────────────────────────────
        in_features = base.head.in_features          # 768
        base.head = nn.Sequential(
            nn.Dropout(p=0.5),
            nn.Linear(in_features, NUM_CLASSES),
        )
        for m in base.head.modules():
            if isinstance(m, nn.Linear):
                nn.init.trunc_normal_(m.weight, std=0.02)
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)

        self.backbone = base

    def forward(self, x):
        return self.backbone(x)


