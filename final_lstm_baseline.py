# CELL 6 — MODEL DEFINITION
# ═══════════════════════════════════════════════════════════════════════════════
# Simple LSTM → Linear classifier.
# Takes the LAST hidden state of the top layer as the sequence representation.
# Architecture follows Korean paper recommendations adapted for smaller data.
# ═══════════════════════════════════════════════════════════════════════════════

class AI4FinLSTM(nn.Module):
    """LSTM baseline for 3-class financial time series classification.

    Architecture:
        Input (batch, seq_len, input_size)
          → LSTM (hidden_size, num_layers, dropout)
          → last hidden state (batch, hidden_size)
          → Dropout(0.5)
          → Linear(hidden_size, 3)
    """
    def __init__(self, input_size, hidden_size=64, num_layers=2, dropout=0.3):
        super().__init__()
        self.lstm = nn.LSTM(
            input_size=input_size,
            hidden_size=hidden_size,
            num_layers=num_layers,
            batch_first=True,
            dropout=dropout if num_layers > 1 else 0.0,
        )
        self.classifier = nn.Sequential(
            nn.Dropout(p=0.5),
            nn.Linear(hidden_size, NUM_CLASSES),
        )
        # Initialize classifier weights
        for m in self.classifier.modules():
            if isinstance(m, nn.Linear):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)

    def forward(self, x):
        # x: (batch, seq_len, input_size)
        lstm_out, (h_n, c_n) = self.lstm(x)
        # h_n: (num_layers, batch, hidden_size) — take last layer
        last_hidden = h_n[-1]  # (batch, hidden_size)
        return self.classifier(last_hidden)


