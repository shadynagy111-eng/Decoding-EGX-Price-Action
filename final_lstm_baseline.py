def train_epoch(model, loader, optimizer, criterion):
    model.train()
    running_loss = 0.0
    all_preds, all_labels = [], []
    for inputs, labels in tqdm(loader, desc='    train', leave=False):
        inputs = inputs.to(DEVICE)
        labels = labels.to(DEVICE)
        optimizer.zero_grad()
        outputs = model(inputs)
        loss = criterion(outputs, labels)
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
        optimizer.step()
        running_loss += loss.item() * inputs.size(0)
        all_preds.append(torch.argmax(outputs.detach(), dim=1).cpu())
        all_labels.append(labels.cpu())
    all_preds  = torch.cat(all_preds).numpy()
    all_labels = torch.cat(all_labels).numpy()
    _, _, macro_f1, _ = precision_recall_fscore_support(
        all_labels, all_preds, average='macro', zero_division=0)
    return running_loss / len(loader.dataset), accuracy_score(all_labels, all_preds), macro_f1


def eval_epoch(model, loader, criterion):
    model.eval()
    running_loss = 0.0
    all_preds, all_labels, all_probs = [], [], []
    with torch.no_grad():
        for inputs, labels in tqdm(loader, desc='    eval ', leave=False):
            inputs = inputs.to(DEVICE)
            labels = labels.to(DEVICE)
            outputs = model(inputs)
            loss = criterion(outputs, labels)
            running_loss += loss.item() * inputs.size(0)
            probs = torch.softmax(outputs.float(), dim=1)
            all_preds.append(torch.argmax(probs, dim=1).cpu())
            all_labels.append(labels.cpu())
            all_probs.append(probs.cpu())
    all_preds  = torch.cat(all_preds).numpy()
    all_labels = torch.cat(all_labels).numpy()
    all_probs  = torch.cat(all_probs).numpy()
    _, _, macro_f1, _ = precision_recall_fscore_support(
        all_labels, all_preds, average='macro', zero_division=0)
    recall_pc = recall_score(all_labels, all_preds, labels=[0,1,2], average=None, zero_division=0)
    try:
        auc = roc_auc_score(all_labels, all_probs, multi_class='ovr', average='macro')
    except ValueError:
        auc = None
    return (running_loss / len(loader.dataset),
            accuracy_score(all_labels, all_preds),
            macro_f1, auc, all_preds, all_labels, all_probs, recall_pc)

