import openpyxl
from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
from openpyxl.utils import get_column_letter

HEADER_FONT = Font(name='Arial', bold=True, color='FFFFFF', size=11)
HEADER_FILL = PatternFill('solid', fgColor='2F5496')
DATA_FONT   = Font(name='Arial', size=10)
GREEN_FILL  = PatternFill('solid', fgColor='C6EFCE')
RED_FILL    = PatternFill('solid', fgColor='FFC7CE')

OUTPUT_XLSX = os.path.join(RESULTS_DIR, 'detailed_model_results_lstm.xlsx')

wb = openpyxl.Workbook()
wb.remove(wb.active)

for v_name, trade_log in all_trade_logs.items():
    # ── Trades sheet ──────────────────────────────────────────────
    trade_cols = [
        'walk_id', 'ticker', 'window_start', 'window_end',
        'fwd_start', 'fwd_end', 'entry_close', 'exit_close',
        'pct_change', 'true_label', 'pred_label', 'correct',
        'confidence', 'prob_bearish', 'prob_bullish', 'prob_neutral',
        'position', 'trade_return',
    ]
    tl = trade_log[trade_cols].copy()
    ws = wb.create_sheet(f'{v_name}_Trades')

    # Write headers
    for c, col_name in enumerate(tl.columns, 1):
        cell = ws.cell(row=1, column=c, value=col_name)
        cell.font = HEADER_FONT
        cell.fill = HEADER_FILL
        cell.alignment = Alignment(horizontal='center', wrap_text=True)

    # Write data
    for r, row_data in enumerate(tl.itertuples(index=False), 2):
        for c, val in enumerate(row_data, 1):
            cell = ws.cell(row=r, column=c)
            if isinstance(val, (np.integer,)): val = int(val)
            elif isinstance(val, (np.floating,)): val = float(val)
            elif isinstance(val, (np.bool_,)): val = bool(val)
            cell.value = val
            cell.font = DATA_FONT

    # Color correct/wrong and trade_return columns
    for r in range(2, len(tl) + 2):
        cell_c = ws.cell(row=r, column=12)  # correct
        cell_c.fill = GREEN_FILL if cell_c.value else RED_FILL
        cell_r = ws.cell(row=r, column=18)  # trade_return
        if isinstance(cell_r.value, (int, float)):
            cell_r.fill = GREEN_FILL if cell_r.value > 0 else (RED_FILL if cell_r.value < 0 else PatternFill())

    # Auto-width
    for c in range(1, len(trade_cols) + 1):
        ws.column_dimensions[get_column_letter(c)].width = max(len(str(trade_cols[c-1])) + 4, 13)
    ws.freeze_panes = 'A2'
    ws.auto_filter.ref = ws.dimensions

    print(f'  {v_name}_Trades: {len(tl):,} rows')

wb.save(OUTPUT_XLSX)
