# ── Compute technical indicators on full history ──────────────────────────────
def compute_technical_indicators(df):
    """Add 5 technical indicators to an OHLCV DataFrame (computed on full history)."""
    close = df['Close'].values.astype(float)
    high  = df['High'].values.astype(float)
    low   = df['Low'].values.astype(float)
    volume = df['Volume'].values.astype(float)
    n = len(df)

    # 1. RSI (14-period)
    delta = np.diff(close, prepend=close[0])
    gain = np.where(delta > 0, delta, 0.0)
    loss = np.where(delta < 0, -delta, 0.0)
    avg_gain = np.full(n, np.nan)
    avg_loss = np.full(n, np.nan)
    if n >= 15:
        avg_gain[14] = gain[1:15].mean()
        avg_loss[14] = loss[1:15].mean()
        for i in range(15, n):
            avg_gain[i] = (avg_gain[i-1] * 13 + gain[i]) / 14
            avg_loss[i] = (avg_loss[i-1] * 13 + loss[i]) / 14
    rs = avg_gain / (avg_loss + 1e-10)
    rsi = 100 - (100 / (1 + rs))
    df['RSI'] = rsi

    # 2. MACD (12, 26, 9)
    ema12 = pd.Series(close).ewm(span=12, adjust=False).mean().values
    ema26 = pd.Series(close).ewm(span=26, adjust=False).mean().values
    macd_line = ema12 - ema26
    signal_line = pd.Series(macd_line).ewm(span=9, adjust=False).mean().values
    df['MACD'] = macd_line
    df['MACD_Signal'] = signal_line

    # 3. Bollinger Band width (20-period, 2 std)
    sma20 = pd.Series(close).rolling(20).mean().values
    std20 = pd.Series(close).rolling(20).std().values
    bb_upper = sma20 + 2 * std20
    bb_lower = sma20 - 2 * std20
    bb_width = (bb_upper - bb_lower) / (sma20 + 1e-10)
    df['BB_Width'] = bb_width

    # 4. Volume SMA ratio (volume / SMA(volume, 20))
    vol_sma20 = pd.Series(volume).rolling(20).mean().values
    df['Vol_Ratio'] = volume / (vol_sma20 + 1e-10)

    return df


