# CELL 5 — DATASET CLASS
# ═══════════════════════════════════════════════════════════════════════════════
# Returns normalized OHLCV (+ optional TI) sequences for each sample.
# Min-max normalization per window:
#   - O/H/L/C normalized together (same price scale)
#   - Volume normalized separately
#   - Each TI normalized separately
# ═══════════════════════════════════════════════════════════════════════════════

OHLCV_COLS = ['Open', 'High', 'Low', 'Close', 'Volume']
TI_COLS    = ['RSI', 'MACD', 'MACD_Signal', 'BB_Width', 'Vol_Ratio']

class OHLCVDataset(Dataset):
    """Dataset that returns normalized OHLCV (± TI) sequences from price files."""

    def __init__(self, df, price_data, use_ti=False):
        self.use_ti = use_ti
        self.samples = []
        skipped = 0

        for _, row in df.iterrows():
            ticker = row['ticker']
            if ticker not in price_data:
                skipped += 1
                continue

            start = int(row['window_row_start'])
            end   = int(row['window_row_end'])
            label = LABEL_TO_IDX[row['label']]
            pct   = float(row['pct_change'])

            pdf = price_data[ticker]
            if end >= len(pdf):
                skipped += 1
                continue

            self.samples.append((ticker, start, end, label, pct))

        if skipped:
            print(f'      {skipped} samples skipped (missing ticker or out of range).')

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        ticker, start, end, label, pct = self.samples[idx]
        pdf = price_data[ticker]
        window = pdf.iloc[start:end+1]

        # ── Extract features ──────────────────────────────────────────
        ohlcv = window[OHLCV_COLS].values.astype(np.float32)

        # ── Min-max normalize OHLC together ───────────────────────────
        price_vals = ohlcv[:, :4]  # O, H, L, C
        p_min, p_max = price_vals.min(), price_vals.max()
        price_norm = (price_vals - p_min) / (p_max - p_min + 1e-8)

        # ── Min-max normalize Volume separately ───────────────────────
        vol = ohlcv[:, 4:5]
        v_min, v_max = vol.min(), vol.max()
        vol_norm = (vol - v_min) / (v_max - v_min + 1e-8)

        features = np.concatenate([price_norm, vol_norm], axis=1)  # (40, 5)

        # ── Optional: add technical indicators ────────────────────────
        if self.use_ti:
            ti_vals = window[TI_COLS].values.astype(np.float32)
            # Replace NaN with 0 (from TI warm-up periods)
            ti_vals = np.nan_to_num(ti_vals, nan=0.0)
            # Min-max normalize each TI independently
            for col_i in range(ti_vals.shape[1]):
                col = ti_vals[:, col_i]
                c_min, c_max = col.min(), col.max()
                ti_vals[:, col_i] = (col - c_min) / (c_max - c_min + 1e-8)
            features = np.concatenate([features, ti_vals], axis=1)  # (40, 10)

        tensor = torch.from_numpy(features)  # (40, input_size)
        return tensor, label

    @property
    def pct_changes(self):
        return [s[4] for s in self.samples]

    @property
    def targets(self):
        return [s[3] for s in self.samples]

