# Loops through each variant, then each walk.
# Trains the LSTM, evaluates on test, runs full inference, saves everything.
# ═══════════════════════════════════════════════════════════════════════════════

walk_ids = sorted(meta_df['walk_id'].unique())
print(f'Total walks: {len(walk_ids)}')

all_variant_results = {}  # variant_name → list of walk result dicts
all_trade_logs = {}       # variant_name → concatenated trade log DataFrame

for variant in VARIANTS:
    v_name  = variant['name']
    use_ti  = variant['use_ti']
    in_size = variant['input_size']

    v_dir = os.path.join(RESULTS_DIR, v_name)
    os.makedirs(v_dir, exist_ok=True)

    print(f"\n{'═'*70}")
    print(f"  VARIANT: {v_name}  (input_size={in_size}, use_ti={use_ti})")
    print(f"{'═'*70}")

    walk_results = []
    walk_trade_logs = []

    for walk_id in walk_ids:
        print(f"\n{'─'*60}")
        print(f"  WALK {walk_id} / {len(walk_ids)}  [{v_name}]")
        print(f"{'─'*60}")

        model_path  = os.path.join(v_dir, f'walk_{walk_id:02d}_best_model.pth')
        log_path    = os.path.join(v_dir, f'walk_{walk_id:02d}_training_log.csv')
        result_path = os.path.join(v_dir, f'walk_{walk_id:02d}_result.csv')
        trade_path  = os.path.join(v_dir, f'walk_{walk_id:02d}_trade_log.csv')

        # ── Graceful resume ───────────────────────────────────────────
        if os.path.exists(model_path) and os.path.exists(result_path) and os.path.exists(trade_path):
            print(f'  Walk {walk_id} already done — loading saved results.')
            walk_results.append(pd.read_csv(result_path).iloc[0].to_dict())
            walk_trade_logs.append(pd.read_csv(trade_path))
            continue

        walk_df  = meta_df[meta_df['walk_id'] == walk_id]
        train_df = walk_df[walk_df['split'] == 'train']
        val_df   = walk_df[walk_df['split'] == 'val']
        test_df  = walk_df[walk_df['split'] == 'test']

        train_ds = OHLCVDataset(train_df, price_data, use_ti=use_ti)
        val_ds   = OHLCVDataset(val_df,   price_data, use_ti=use_ti)
        test_ds  = OHLCVDataset(test_df,  price_data, use_ti=use_ti)

        print(f'  Data — Train: {len(train_ds):,} | Val: {len(val_ds):,} | Test: {len(test_ds):,}')

        train_loader = DataLoader(train_ds, batch_size=BATCH_SIZE, shuffle=True,
                                  num_workers=0, pin_memory=True)
        val_loader   = DataLoader(val_ds,   batch_size=BATCH_SIZE, shuffle=False,
                                  num_workers=0, pin_memory=True)
        test_loader  = DataLoader(test_ds,  batch_size=BATCH_SIZE, shuffle=False,
                                  num_workers=0, pin_memory=True)

        # ── Model ─────────────────────────────────────────────────────
        model = AI4FinLSTM(input_size=in_size, hidden_size=HIDDEN_SIZE,
                           num_layers=NUM_LAYERS, dropout=DROPOUT).to(DEVICE)

        # ── Class weighting (same as image models) ────────────────────
        class_counts  = np.bincount(train_ds.targets, minlength=NUM_CLASSES)
        class_weights = len(train_ds.targets) / (NUM_CLASSES * class_counts + 1e-6)
        criterion = nn.CrossEntropyLoss(weight=torch.FloatTensor(class_weights).to(DEVICE))

        optimizer = optim.AdamW(model.parameters(), lr=LR, weight_decay=WEIGHT_DECAY)
        scheduler = optim.lr_scheduler.ReduceLROnPlateau(
            optimizer, mode='max', factor=0.3, patience=7, min_lr=1e-7)

        best_val_f1 = 0.0
        best_epoch  = 1
        patience_counter = 0
        best_state  = None
        history     = []

        for epoch in range(1, MAX_EPOCHS + 1):
            tr_loss, tr_acc, tr_f1 = train_epoch(model, train_loader, optimizer, criterion)
            vl_loss, vl_acc, vl_f1, vl_auc, _, _, _, recall_pc = eval_epoch(
                model, val_loader, criterion)

            scheduler.step(vl_f1)
            lr = optimizer.param_groups[0]['lr']

            print(f"  Ep {epoch:02d}/{MAX_EPOCHS} | "
                  f"TrLoss={tr_loss:.4f} TrF1={tr_f1:.4f} | "
                  f"VLoss={vl_loss:.4f} VF1={vl_f1:.4f} "
                  f"VAUC={f'{vl_auc:.4f}' if vl_auc is not None else 'N/A'} | "
                  f"Rec: Be={recall_pc[0]:.2f} Bu={recall_pc[1]:.2f} Ne={recall_pc[2]:.2f} | "
                  f"LR={lr:.2e}")

            history.append({
                'walk_id': walk_id, 'epoch': epoch,
                'train_loss': tr_loss, 'train_acc': tr_acc, 'train_f1': tr_f1,
                'val_loss': vl_loss, 'val_acc': vl_acc, 'val_f1': vl_f1,
                'val_auc': vl_auc, 'lr': lr,
            })

            if vl_f1 > best_val_f1:
                best_val_f1 = vl_f1
                best_epoch  = epoch
                best_state  = {k: v.cpu().clone() for k, v in model.state_dict().items()}
                patience_counter = 0
                print(f'    ★ New best F1={vl_f1:.4f}')
            else:
                patience_counter += 1

            if patience_counter >= PATIENCE_ES:
                print(f'  Early stopping at epoch {epoch}.')
                break

        # ── Save training log & plots ─────────────────────────────────
        history_df = pd.DataFrame(history)
        history_df.to_csv(log_path, index=False)
        plot_training_curves(history_df, walk_id, best_epoch, v_dir, v_name)

        # ── Load best model & evaluate on test ────────────────────────
        model.load_state_dict({k: v.to(DEVICE) for k, v in best_state.items()})
        torch.save(best_state, model_path)

        te_loss, te_acc, te_f1, te_auc, te_preds, te_labels, te_probs, _ = \
            eval_epoch(model, test_loader, criterion)

        plot_confusion_matrix(te_labels, te_preds, walk_id, v_dir, v_name)

        kappa = cohen_kappa_score(te_labels, te_preds)
        mcc   = matthews_corrcoef(te_labels, te_preds)
        print(f'\n  Classification Report — Walk {walk_id} ({v_name}):')
        print(classification_report(te_labels, te_preds, target_names=CLASS_NAMES, zero_division=0))

        sharpe, max_dd, pf, wr, n_trades = financial_metrics(
            te_preds, np.array(test_ds.pct_changes))

        walk_result = {
            'walk_id': walk_id, 'best_val_f1': best_val_f1,
            'test_accuracy': te_acc, 'test_macro_f1': te_f1, 'test_auc': te_auc,
            'cohen_kappa': kappa, 'mcc': mcc,
            'sharpe_ratio': sharpe, 'max_drawdown': max_dd,
            'profit_factor': pf, 'win_rate': wr, 'total_trades': n_trades,
        }
        walk_results.append(walk_result)
        pd.DataFrame([walk_result]).to_csv(result_path, index=False)

        # ── Full inference for trade log ──────────────────────────────
        print(f'  Running full inference for trade log...')
        trade_log = run_full_inference(model, test_df, price_data, use_ti)
        trade_log['walk_id'] = walk_id
        trade_log.to_csv(trade_path, index=False)
        walk_trade_logs.append(trade_log)

        print(f'  Walk {walk_id}: Acc={te_acc:.4f} F1={te_f1:.4f} '
              f'Sharpe={sharpe:.4f} Trades={n_trades}')

        # ── Cleanup ───────────────────────────────────────────────────
        del model, optimizer, scheduler, criterion
        del train_loader, val_loader, test_loader
        del train_ds, val_ds, test_ds
        torch.cuda.empty_cache()

    # ── Store results for this variant ────────────────────────────────
    all_variant_results[v_name] = walk_results
    all_trade_logs[v_name] = pd.concat(walk_trade_logs, ignore_index=True)

print(f'\n\nAll variants complete.')

