@torch.no_grad()
def run_full_inference(model, test_df_walk, price_data, use_ti):
    """Run inference on every test sample and return a trade log DataFrame."""
    model.eval()
    results = []

    for _, row in test_df_walk.iterrows():
        ticker = row['ticker']
        if ticker not in price_data:
            continue

        start = int(row['window_row_start'])
        end   = int(row['window_row_end'])
        pdf   = price_data[ticker]
        if end >= len(pdf):
            continue

        # Build input tensor (same logic as Dataset)
        window = pdf.iloc[start:end+1]
        ohlcv = window[OHLCV_COLS].values.astype(np.float32)

        price_vals = ohlcv[:, :4]
        p_min, p_max = price_vals.min(), price_vals.max()
        price_norm = (price_vals - p_min) / (p_max - p_min + 1e-8)
        vol = ohlcv[:, 4:5]
        v_min, v_max = vol.min(), vol.max()
        vol_norm = (vol - v_min) / (v_max - v_min + 1e-8)
        features = np.concatenate([price_norm, vol_norm], axis=1)

        if use_ti:
            ti_vals = window[TI_COLS].values.astype(np.float32)
            ti_vals = np.nan_to_num(ti_vals, nan=0.0)
            for col_i in range(ti_vals.shape[1]):
                col = ti_vals[:, col_i]
                c_min, c_max = col.min(), col.max()
                ti_vals[:, col_i] = (col - c_min) / (c_max - c_min + 1e-8)
            features = np.concatenate([features, ti_vals], axis=1)

        x = torch.from_numpy(features).unsqueeze(0).to(DEVICE)  # (1, 40, F)
        logits = model(x)
        probs = torch.softmax(logits.float(), dim=1).cpu().numpy()[0]
        pred_idx = probs.argmax()

        # Position: +1 bull, -1 bear, 0 neutral
        position = {0: -1, 1: 1, 2: 0}[pred_idx]
        trade_ret = position * float(row['pct_change'])

        results.append({
            'walk_id':      int(row.get('walk_id', 0)),
            'ticker':       ticker,
            'window_start': row['window_start'],
            'window_end':   row['window_end'],
            'fwd_start':    row['fwd_start'],
            'fwd_end':      row['fwd_end'],
            'entry_close':  float(row['entry_close']),
            'exit_close':   float(row['exit_close']),
            'pct_change':   float(row['pct_change']),
            'true_label':   row['label'],
            'pred_label':   IDX_TO_LABEL[pred_idx],
            'correct':      row['label'] == IDX_TO_LABEL[pred_idx],
            'confidence':   float(probs[pred_idx]),
            'prob_bearish': float(probs[0]),
            'prob_bullish': float(probs[1]),
            'prob_neutral': float(probs[2]),
            'position':     position,
            'trade_return': trade_ret,
        })

    return pd.DataFrame(results)

